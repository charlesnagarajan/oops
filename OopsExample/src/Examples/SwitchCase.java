package Examples;

public class SwitchCase {

	public static void main(String[] args) {

		int x = 5;
		switch (x) {
		case 1:
			x += 5;
			break;
		case 2:
			x += 10;
			break;
		case 3:
			x += 15;
			break;
		case 4:
			x += 20;
			break;

		}
		System.out.println(x);

	}

}
